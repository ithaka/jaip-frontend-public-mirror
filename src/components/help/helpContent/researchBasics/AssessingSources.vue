      Is the author’s affiliation apparent, such as where he or she works? If the author is a
      professor, are advanced degrees listed alongside his or her academic position?
    </li>
    <li>
      Does the author have qualifications to write on the topic, such as an advanced degree in the
      field with several years of experience and research on the topic?
    </li>
    <li>
      Does the publication have an author and an editor? The presence of an editor might imply a
      collection of articles, as opposed to a single piece by a single person. Be sure to note who
      the piece is actually written by. This should usually be a person in scholarly works with
      appropriate credentials.
    </li>
    <li>
      Occasionally, an organization will serve as the author. In these cases, make sure that the
      organization has expertise and credibility in the field. You can usually determine this by
      reading the organization’s bio, the same way you would read about an author.
    </li>
