    Look at the bibliography: is the article formulated from other scholarship? If there is no
    bibliography, that could be a sign that a source is not scholarly.
  </p>
  <pep-pharos-heading id="currency" :level="4" preset="2">Currency</pep-pharos-heading>
  <p>
    Once you’ve determined that a work is scholarly, it’s important that you make sure the sources
    you use are also current and relevant. When you assess the currency of a source, you’re asking,
    “How new is this information? Has anything changed since this was published?”
  </p>
  <p>Here are a few clues that can help you decide whether a source is current.<br /></p>
  <ul>
    <li>
      Publication or revision date: does the source have one? If not, you really have no way to tell
      whether it’s outdated. A scholarly source should always include a publication date.<br />
    </li>
    <li>
      How recent are the publication dates of the works cited in its bibliography? If a new article
      cites only articles from 20 years ago, consider whether it might be omitting more recent
      sources.<br />
    </li>
    <li>
      Has the information been revised, updated, or retracted? Is newer information available?<br />
    </li>
    <li>
      Also consider your topic: is information about it still changing or being discovered,such as
      in the fields of medicine or technology? If so, you may want to look for only very current
      sources. On the other hand, if you are writing about an event in ancient history, it may be
      fine to use older sources.<br />
    </li>
  </ul>
  <pep-pharos-heading id="relevance" :level="4" preset="2">Relevance</pep-pharos-heading>
  <p>
    The question of relevance is one that only you can answer: how useful is a source for your
    purpose?<br />
  </p>
  <ul>
    <li>Is it written at an appropriate level?<br /></li>
    <li>Is it too broad or too specific?<br /></li>
    <li>Does it answer a question or fill a need in your research?<br /></li>
    <li>Does it add something new?<br /></li>
  </ul>
