  <pep-pharos-heading :level="4" preset="2">Bias</pep-pharos-heading>
  <p>
    It’s important that academic scholarship is presented in an objective manner without bias. The
    peer review system is an established way to identify biases that might appear in scholarly
    research. In peer review, a work is evaluated by experts and published only if it meets the
    discipline’s standards. It is also a way to ensure objectivity in assessing an academic article.
    The peers selected for the review are:
  </p>
  <ul>
    <li>In the same field as the author(s)</li>
    <li>Proven to be objective and independent from the work they are reviewing for publication</li>
    <li>
      Familiar enough with the body of work the article relates to in order to lend credibility to
      the publication of it
    </li>
    <li>Able to make a formal declaration in the work attesting to their neutrality</li>
  </ul>
