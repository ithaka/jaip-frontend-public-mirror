  <pep-pharos-heading :level="4" preset="2">Authority</pep-pharos-heading>
  <p>
    To have authority means to be recognized as knowledgeable on a specific topic. There’s a root
    word in “authority” that tells you what you’re going to look at first: “author.” When you’re
    considering a source, try to find the answers to these questions:
  </p>
  <ul>
    <li>Is the author or authors clearly identified?</li>
    <li>
