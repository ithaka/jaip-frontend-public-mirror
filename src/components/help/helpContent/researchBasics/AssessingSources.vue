  <p>
    By examining the content, you can determine an article’s suitability for scholarly research.
    Remember to look not only for relevant topical information, but also signs of scholarship. Here
    are some questions to consider:
  </p>
  <ul>
    <li>
      <strong>Audience</strong>: is the work intended for other scholars or experts in the field?
      Does it use discipline-specific terminology?<br />
    </li>
    <li>
      <strong>Intent</strong>: why is the author publishing the article? Is it a report, a case
      study, or an in-depth analysis of a specific topic?<br />
    </li>
    <li>
      <strong>Level</strong>: does the article assume some basic knowledge on the readers’ part? Is
      it written for peers, or to introduce the topic to laypersons?<br />
    </li>
    <li>
      <strong>Style</strong>: Are there illustrations, charts, and tables? If so, are they relevant
      to the content or are they “eye candy” meant to attract readers?<br />
    </li>
    <li>
      <strong>Tone</strong>: is it written using a professional tone and an academic style, or is it
      colloquial and casual, which is a sign it’s not scholarly?<br />
    </li>
  </ul>
